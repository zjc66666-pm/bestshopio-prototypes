window.I18N.addRules([
  // 列表筛选标签：静态字段名 + ": 值"（× 是相邻独立节点，不在本文本节点内）
  // Menu：'Menu title: <kw>'
  { re: /^Menu title:\s*(.+)$/, zh: function (m) { return "导航菜单标题：" + m[1]; } },
  // Blog：'Blog title: <kw>' / 'Author: <kw>'
  { re: /^Blog title:\s*(.+)$/, zh: function (m) { return "博客标题：" + m[1]; } },
  { re: /^Author:\s*(.+)$/, zh: function (m) { return "作者：" + m[1]; } },
  // Page：'Page title: <kw>'
  { re: /^Page title:\s*(.+)$/, zh: function (m) { return "标题：" + m[1]; } },
  // Blog/Page 状态标签：'Status: Visible, Hidden'（逗号分隔的可见值）
  { re: /^Status:\s*(.+)$/, zh: function (m) {
      var map = { "Visible": "可见", "Hidden": "隐藏" };
      var val = m[1].split(",").map(function (s) { var k = s.trim(); return map[k] || k; }).join("、");
      return "状态：" + val;
    } },
  // 列表底部 'Total N records'（单一文本节点）
  { re: /^Total\s+(\d+)\s+records$/, zh: function (m) { return "共 " + m[1] + " 条记录"; } },
  // Page 缺失页：'No page exists for id "X".'
  { re: /^No page exists for id "(.+)"\.$/, zh: function (m) { return "不存在 id 为「" + m[1] + "」的页面。"; } },
  // Menu 缺失页标题：'Menu #X'
  { re: /^Menu #(.+)$/, zh: function (m) { return "导航菜单 #" + m[1]; } }
]);
